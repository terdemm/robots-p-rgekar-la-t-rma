function createList(items) {
  return items.map((item) => `<li>${item}</li>`).join("");
}

function createSpecs(specs) {
  return specs.map(([label, value]) => `
    <div class="spec-row">
      <span>${label}</span>
      <strong>${value}</strong>
    </div>
  `).join("");
}

function createPhotos(product) {
  return product.images.map((image, index) => `
    <a class="mini-photo" href="${image}" target="_blank" rel="noopener noreferrer" aria-label="${product.name} fotoğraf ${index + 1}">
      <img src="${image}" alt="${product.name} ürün fotoğrafı ${index + 1}" loading="lazy">
    </a>
  `).join("");
}

